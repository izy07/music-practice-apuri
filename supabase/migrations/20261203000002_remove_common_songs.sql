-- 全楽器共通の曲（きらきら星など）を削除
-- 実行日: 2025-12-03

-- 全楽器共通の初心者向け曲を削除
DELETE FROM representative_songs 
WHERE title IN (
  'きらきら星',
  'メリーさんの羊',
  'かえるの歌',
  'チューリップ',
  'ロンドン橋',
  'むすんでひらいて',
  'ちょうちょう',
  '春の小川'
);

-- 全楽器共通の中級者向け曲を削除（楽器固有の曲でないもの）
DELETE FROM representative_songs 
WHERE title IN (
  'アニー・ローリー',
  'ロング・ロング・アゴー',
  'アマリリス'
);

-- 注意: 以下の曲は楽器固有の編曲や演奏があるため、楽器ごとに判断が必要
-- - '愛のあいさつ' (エルガー) - バイオリン版は残す
-- - 'G線上のアリア' (バッハ) - バイオリン版は残す
-- - 'カノン' (パッヘルベル) - 楽器ごとに判断
-- - '四季「春」' (ヴィヴァルディ) - バイオリン版は残す
-- - 'ユーモレスク' (ドヴォルザーク) - バイオリン版は残す

-- 確認: 削除された曲の数を表示
DO $$
DECLARE
  deleted_count INTEGER;
BEGIN
  GET DIAGNOSTICS deleted_count = ROW_COUNT;
  RAISE NOTICE '全楽器共通の曲 % 曲を削除しました', deleted_count;
END $$;

